package enums;
/**
 * Enum for type of Saving Throw a creature has to make in DnD after having a spell cast on them
 * * NONE for if no saving throw is required (tons of spells don't)
 * * STR for Strength
 * * DEX for Dexterity
 * * CON for Constitution
 * * INT for Intelligence
 * * WIS for Wisdom
 * * CHA for Charisma
 */
public enum saveType
{
	NONE,
	STR,
	DEX,
	CON,
	INT,
	WIS,
	CHA
}
